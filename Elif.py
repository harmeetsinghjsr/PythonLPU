week=int(input("Enter weekday number from 1 to 7 "))
if week==1:
    print("\nMonday ")
elif week==2:
    print("\nTuesday ")
elif week==3:
    print("\nWednesday ")
elif week==4:
    print("\nThursday ")
elif week==5:
    print("\nFriday ")
elif week==6:
    print("\nSaturday ")
elif week==7:
    print("\nSunday ")
else:
    print("\nInvalid Input,\nPlease Enter weekday number ranging from 1-7 ")
